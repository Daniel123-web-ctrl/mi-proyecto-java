
public class mouredev2 {

    
    public static void main(String[] args) {
       //Sentencia switch              la condicion de switch sirve para acabar revisando  el valor de una variable 
       var day=1;
       switch(day){
           case 1: // cual es el valor que nosotros queremos  saber si se aplica a la condicion 
           System.out.println("lunes"); //que si day vale 1 quiero que nos imprima lunes 
           break; 
           case 2:
           System.out.println("martes");
           break;
           case 3:
           System.out.println("miercoles");
           break;
           default:
               System.out.println("no es lunes martes o miercoles");
       }
             
               
       
       
       
       
       
       
    }
    
}
